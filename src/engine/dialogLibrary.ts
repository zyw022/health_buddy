function pickRandom<T>(arr: T[]): T {
  return arr[Math.floor(Math.random() * arr.length)]
}

export function getPetMessage(personality: Personality, action: PetAction): string {
  return pickRandom(DIALOGS[personality][action] ?? DIALOGS.friend.idle)
}

export function getInteractionMessage(personality: Personality): string {
  return pickRandom(INTERACTION_LINES[personality] ?? INTERACTION_LINES.friend)
}
